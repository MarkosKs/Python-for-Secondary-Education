A = int(input("Δώσε μου το βαθμό του Α τετραμήνου :  "))
B = int(input("Δώσε μου το βαθμό του Β τετραμήνου :  "))
E = int(input("Δώσε μου το βαθμό Εξετάσεων Ιουνίου :  "))
MO = (A + B + E) / 3
print("Ο Μέσος Όρος σου στο μάθημα είναι : ")
print(MO)
if MO >= 10:
  print("Περνάς το μάθημα!")
if MO < 10:
  print("Πρέπει να δώσεις εξετάσεις το Σεπτέμβριο!")
