# ΑΣΚΗΣΕΙΣ: Συμβολοσειρές (Strings)

# Άσκηση 1: Ζήτησε από τον χρήστη ένα όνομα και εμφάνισέ το σε όλα κεφαλαία και όλα πεζά.
name = input("Όνομα: ")
print(name.upper())
print(name.lower())

# Άσκηση 2: Εμφάνισε τον πρώτο και τον τελευταίο χαρακτήρα μιας λέξης.
word = input("Λέξη: ")
print("Πρώτο γράμμα:", word[0])
print("Τελευταίο γράμμα:", word[-1])

# Άσκηση 3: Έλεγξε αν μια λέξη περιέχει μια υποσυμβολοσειρά.
text = input("Κείμενο: ")
substring = input("Υποσυμβολοσειρά: ")
print(substring in text)

# Άσκηση 4: Αντιστροφή μιας συμβολοσειράς.
text = input("Κείμενο: ")
print(text[::-1])

# Άσκηση 5: Μέτρησε πόσες φορές εμφανίζεται ένας χαρακτήρας σε μια συμβολοσειρά.
text = input("Κείμενο: ")
char = input("Χαρακτήρας: ")
print(text.count(char))