#Αν μας δινόταν μια ακολουθία γραμμών μορφοποιημένων ως εξής:
#From stephen.marquard@uct.ac.za Sat Jan 5 09:14:16 2008
#και θέλαμε να εξάγουμε μόνο το δεύτερο μισό της διεύθυνσης (δηλαδή το uct.ac.za)
#από κάθε γραμμή, θα μπορούσαμε να το κάνουμε χρησιμοποιώντας τη μέθοδο find
#και την διαμέριση συμβολοσειράς. Αρχικά, θα βρούμε τη θέση του συμβόλου at ( @ )
#στη συμβολοσειρά. Στη συνέχεια, θα βρούμε τη θέση του πρώτου διαστήματος μετά
#το σύμβολο at και, στη συνέχεια, θα χρησιμοποιήσουμε τη διαμέριση συμβολοσειράς
#για να εξαγάγουμε το τμήμα της συμβολοσειράς που αναζητούμε. 
data = 'From stephen.marquard@uct.ac.za Sat Jan 5 09:14:16 2008' 
θέσηat = data.find('@') 
print(θέσηat)
θέση_τέλους = data.find(' ',θέσηat) 
print(θέση_τέλους) 
host = data[θέσηat+1:θέση_τέλους] 
print(host)

