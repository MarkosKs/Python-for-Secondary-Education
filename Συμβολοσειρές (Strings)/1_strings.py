#Πάρτε τον ακόλουθο κώδικα Python που αποθηκεύει μια συμβολοσειρά: str = 'X-DSPAM-Confidence: 0.8475'
#Χρησιμοποιήστε find και διαμέριση συμβολοσειράς για να εξαγάγετε το τμήμα της συμβολοσειράς
#μετά τον χαρακτήρα άνω και κάτω τελείας και στη συνέχεια χρησιμοποιήστε
#τη συνάρτηση float για να μετατρέψετε τη συμβολοσειρά που εξαγάγατε σε αριθμό κινητής υποδιαστολής.

str = 'X-DSPAM-Confidence: 0.8475'

# Εύρεση της θέσης της άνω και κάτω τελείας
pos = str.find(':')

# Εξαγωγή του κομματιού μετά την άνω και κάτω τελεία
number_str = str[pos + 1:].strip()

# Μετατροπή σε float
number = float(number_str)

# Εμφάνιση αποτελέσματος
print(number)

