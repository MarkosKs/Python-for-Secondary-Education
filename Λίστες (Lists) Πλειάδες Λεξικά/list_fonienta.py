lw=['ΚΑΛΗΜΕΡΑ','ΑΝΑΝΤΙΚΑΤΑΣΤΑΤΟ','ΠΑΡΑΠΟΙΗΣΗ','ΥΠΕΡΑΙΩΝΟΒΙΟ','ΑΝΤΙΣΤΟΙΧΟ','ΑΥΡΙΟ', 'ΑΑΑΑΑΑΑΑΑΑΑΑΑΑΑΑΑΑ']
print('Περισσότερα φωνήεντα:',maxxv(lw))
