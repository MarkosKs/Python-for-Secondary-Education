# Να γράψετε πρόγραμμα σε Python το οποίο:
# α) να διαβάζει το επώνυμο και το μισθό 5 υπαλλήλων μιας εταιρείας και να τα καταχωρίζει
# στις λίστες EP και MIS αντίστοιχα
# β) να υπολογίζει και να εμφανίζει το ΜΟ όλων των μισθών
# γ) να υπολογίζει και να εμφανίζει το μέγιστο μισθό και το επώνυμο του υπαλλήλου που τον
# έχει
# δ) με τη βοήθεια της bubblesort να ταξινομεί τις λίστες με αύξουσα σειρά ως προς το μισθό
# και να εμφανίζει στην οθόνη όλους τους υπαλλήλους με το μισθό τους από δίπλα (σε
# αύξουσα σειρά)
# ε) να εμφανίζει το επώνυμο των υπαλλήλων που έχουν μισθό πάνω από το ΜΟ
# στ) να εμφανίζει το επώνυμο και το μισθό των τριών υπαλλήλων με το μικρότερο μισθό
# (θεωρούμε ότι είναι μόνο 3)



EP = []  # Λίστα για τα επώνυμα
MIS = []  # Λίστα για τους μισθούς

for i in range(5):
    epwnymo = input(f"Δώστε το επώνυμο του υπαλλήλου {i+1}: ")
    misthos = float(input(f"Δώστε το μισθό του υπαλλήλου {i+1}: "))
    EP.append(epwnymo)
    MIS.append(misthos)

# β) Υπολογισμός και εμφάνιση του ΜΟ όλων των μισθών
MO = sum(MIS) / len(MIS)
print(f"Ο ΜΟ των μισθών είναι: {MO:.2f}")

# γ) Υπολογισμός και εμφάνιση του μέγιστου μισθού και του επωνύμου του υπαλλήλου
max_salary = max(MIS)
index_max = MIS.index(max_salary)
print(f"Ο υπάλληλος με τον μέγιστο μισθό είναι: {EP[index_max]} με μισθό {max_salary}")

# δ) Ταξινόμηση των λιστών με τη μέθοδο Bubble Sort κατά αύξουσα σειρά των μισθών
for i in range(len(MIS)):
    for j in range(0, len(MIS) - i - 1):
        if MIS[j] > MIS[j + 1]:
            # Ανταλλαγή των μισθών
            MIS[j], MIS[j + 1] = MIS[j + 1], MIS[j]
            # Ανταλλαγή των επωνύμων
            EP[j], EP[j + 1] = EP[j + 1], EP[j]

# Εμφάνιση των υπαλλήλων με τους μισθούς τους σε αύξουσα σειρά
print("Οι υπάλληλοι με τους μισθούς τους σε αύξουσα σειρά:")
for i in range(5):
    print(f"{EP[i]}: {MIS[i]:.2f}")

# ε) Εμφάνιση των επωνύμων των υπαλλήλων με μισθό πάνω από τον ΜΟ
print("Οι υπάλληλοι με μισθό πάνω από τον ΜΟ είναι:")
for i in range(5):
    if MIS[i] > MO:
        print(EP[i])

# στ) Εμφάνιση των επωνύμων και των μισθών των 3 υπαλλήλων με τον μικρότερο μισθό
print("Οι τρεις υπάλληλοι με τον μικρότερο μισθό είναι:")
for i in range(3):
    print(f"{EP[i]}: {MIS[i]:.2f}")
