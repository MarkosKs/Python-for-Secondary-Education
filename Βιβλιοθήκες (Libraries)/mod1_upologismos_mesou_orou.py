# Φτιάξτε module(βιβλιοθήκη) με τρεις συναρτήσεις που θα υπολογίζουν το μέσο όρο δύο τριών και τεσσάρων αριθμών αντίσοτιχα.
# Στο κυρίως πρόγραμμα γίνεται ερώτηση για το πλήθος των αριθμών στο χρήστη και στη συνέχεια εισάγονται οι αριθμοί
# και καλείται η αντίστοιχη συνάρτηση αφού πρώτα γίνει import του module.

def duo(a,b):
    mo=(a+b)/2
    return mo

def trio(a,b,c):
    mo=(a+b+c)/3
    return mo
def quatro(a,b,c,d):
    mo=(a+b+c+d)/4
    return mo
