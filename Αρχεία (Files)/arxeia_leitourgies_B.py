# Μέτρημα γραμμών αρχείου
# Εκτύπωση γραμμής με κριτήριο

fname = input('Εισαγάγετε το όνομα του αρχείου: ') 

# έλεγχος ύπαρξης του αρχείου
try: 
    fhand = open(fname) 
except: 
    print('Δεν είναι δυνατό το άνοιγμα του αρχείου:', fname) 
    exit() 

πλήθος = 0 
for γραμμή in fhand: 
    πλήθος = πλήθος + 1
    if γραμμή.startswith('R'): 
        print(γραμμή)
    if γραμμή.find('the') != -1:
        print(γραμμή) 
print('Πλήθος γραμμών:', πλήθος)